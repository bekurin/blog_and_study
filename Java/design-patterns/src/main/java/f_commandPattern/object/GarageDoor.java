package f_commandPattern.object;

public class GarageDoor {

    public void up() {
        System.out.println("차고 문을 엽니다!");
    }

    public void down() {
        System.out.println("차고 문을 닫습니다!!");
    }

    public void stop() {
        System.out.println("차고 문의 움직임을 멈춥니다!!");
    }

    public void lightOn() {
        System.out.println("차고 불을 켭니다!!");
    }

    public void lightOff() {
        System.out.println("차고 불을 끕니다!!");
    }
}
